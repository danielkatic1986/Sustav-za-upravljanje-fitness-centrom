INSERT INTO izvrsena_placanja (id_placanje, id_clanarine, status_tf)
SELECT
    p.id AS id_placanje,
    c.id AS id_clanarine,
    CASE
        WHEN p.status_placanja = 'placeno' THEN TRUE
        ELSE FALSE
    END AS status_tf
FROM placanje p
JOIN racun r         ON r.id = p.id_racun
JOIN racun_stavka rs ON rs.id_racun = r.id
JOIN clanarina c     ON c.id_clan = p.id_clan
JOIN tip_clanarine tc ON tc.id = rs.id_tip_clanarine
WHERE rs.id_tip_clanarine IS NOT NULL
  AND p.status_placanja IN ('placeno', 'blokirano', 'nije_placeno');


